//Q2)write a java program to find square of a given number
import java.util.Scanner;
class Example4
{
	public static void main(String[]args)
	{
		Scanner sc=new Scanner(System.in);
		System.out.println("Enter a number:");
		int n=sc.nextInt();
		//logic
		int square=n*n;
		System.out.println("the cube of a given number is="+square);
	}
}